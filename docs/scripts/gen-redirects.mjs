#!/usr/bin/env node
// Generates .vitepress/dist/_redirects for Cloudflare Pages:
//   - one 301 per built VitePress page, mapping its legacy .html URL to the
//     clean URL (e.g. /PartF.html -> /PartF, /a/index.html -> /a/)
//   - SPA fallbacks (200 rewrites) for each Slidev deck under /slides[-old]/*
// Everything must live in this single root file: Cloudflare Pages ignores
// _redirects placed in subfolders. Deriving the page list from the emitted
// *.html keeps the legacy redirects exactly in sync with the sitemap.
//
// Historical .html URLs with no matching page (e.g. /Part02-CS.html) are
// intentionally absent, so they keep returning 404 instead of redirecting.

import { readdirSync, statSync, existsSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const distDir = join(here, "..", ".vitepress", "dist");

// Top-level dist folders that hold Slidev SPAs, not VitePress pages.
const slideRoots = new Set(["slides", "slides-old", "slides-shots"]);

function walk(dir, base = "") {
  const out = [];
  for (const name of readdirSync(dir)) {
    const abs = join(dir, name);
    const rel = base ? `${base}/${name}` : name;
    if (statSync(abs).isDirectory())
      out.push(...walk(abs, rel));
    else if (name.endsWith(".html"))
      out.push(rel);
  }
  return out;
}

function isSlidePath(rel) {
  return slideRoots.has(rel.split("/")[0]);
}

if (!existsSync(distDir)) {
  console.error(`gen-redirects: dist not found at ${distDir}; run the build first.`);
  process.exit(1);
}

const htmlFiles = walk(distDir);

// Legacy .html -> clean URL, one static 301 per page.
const pageRules = [];
for (const rel of htmlFiles) {
  if (rel === "404.html" || isSlidePath(rel))
    continue;
  const legacy = `/${rel}`;
  const clean = rel === "index.html"
    ? "/"
    : rel.endsWith("/index.html")
      ? `/${rel.slice(0, -"index.html".length)}`
      : `/${rel.slice(0, -".html".length)}`;
  pageRules.push(`${legacy}  ${clean}  301`);
}
pageRules.sort();

// SPA fallbacks: a bare-deck -> trailing-slash 301 plus a splat rewrite that
// serves the deck's index.html for any in-deck route the file system misses.
const deckRules = [];
for (const root of ["slides", "slides-old"]) {
  const rootDir = join(distDir, root);
  if (!existsSync(rootDir))
    continue;
  for (const name of readdirSync(rootDir)) {
    if (!existsSync(join(rootDir, name, "index.html")))
      continue;
    const deck = `/${root}/${name}`;
    deckRules.push(`${deck}  ${deck}/  301`);
    deckRules.push(`${deck}/*  ${deck}/index.html  200`);
  }
}
deckRules.sort();

// Explicit redirects for renamed pages (old clean URL -> new clean URL).
const renameRules = [
  "/MCP  /mcp-server  301",
];

const lines = [
  "# Generated by scripts/gen-redirects.mjs — do not edit by hand.",
  "",
  "# Renamed pages.",
  ...renameRules,
  "",
  "# Slidev deck SPA fallbacks.",
  ...deckRules,
  "",
  "# Legacy .html URLs -> extensionless canonical URLs.",
  ...pageRules,
  "",
];

writeFileSync(join(distDir, "_redirects"), lines.join("\n"), "utf-8");
console.log(`gen-redirects: wrote ${pageRules.length} page redirects and ` +
  `${deckRules.length / 2} deck fallbacks to dist/_redirects`);
